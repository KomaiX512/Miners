    def _identify_engagement_drivers(self, profile_data: Dict) -> List[str]:
        """Identify what drives engagement for this specific account"""
        posts = profile_data.get("posts", [])
        
        if not posts:
            return ["high_quality_visuals", "relevant_hashtags", "engaging_captions"]
        
        # Sort posts by engagement
        sorted_posts = sorted(posts, key=lambda x: x.get("likes", 0) + x.get("comments", 0), reverse=True)
        top_posts = sorted_posts[:min(5, len(posts))]
        bottom_posts = sorted_posts[-min(3, len(posts)):]
        
        drivers = []
        
        # Analyze top performing posts
        top_avg_caption_length = np.mean([len(post.get("caption", "")) for post in top_posts])
        bottom_avg_caption_length = np.mean([len(post.get("caption", "")) for post in bottom_posts])
        
        if top_avg_caption_length > bottom_avg_caption_length * 1.5:
            drivers.append("detailed_captions")
        elif top_avg_caption_length < bottom_avg_caption_length * 0.7:
            drivers.append("concise_captions")
        
        # Check for CTA usage
        top_cta_usage = sum(1 for post in top_posts if self._has_strong_cta(post.get("caption", "")))
        if top_cta_usage / len(top_posts) > 0.6:
            drivers.append("strong_call_to_action")
            
        # Check for personal touch
        top_personal = sum(1 for post in top_posts if self._has_personal_touch(post.get("caption", "")))
        if top_personal / len(top_posts) > 0.5:
            drivers.append("personal_connection")
            
        # Check for timing mentions
        top_trending = sum(1 for post in top_posts if self._mentions_trends(post.get("caption", "")))
        if top_trending / len(top_posts) > 0.4:
            drivers.append("trend_awareness")
        
        return drivers if drivers else ["high_quality_visuals", "relevant_hashtags", "engaging_captions"]
        
