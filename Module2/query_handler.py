    def _parse_transformation_response(self, response_text: str, post_key: str) -> Optional[Dict]:
        """Parse AI response into the exact format required by user"""
        try:
            # Extract JSON from response
            json_start = response_text.find("{")
            json_end = response_text.rfind("}") + 1
            
            if json_start >= 0 and json_end > json_start:
                json_str = response_text[json_start:json_end]
                data = json.loads(json_str)
                
                # Validate required fields
                required_fields = ["text", "hashtags", "cta", "image_prompt"]
                if all(field in data for field in required_fields):
                    # Create the exact format required by user (case-sensitive)
                    transformed_post = {
                        "caption": data["text"],
                        "hashtag": " ".join(data["hashtags"]) if isinstance(data["hashtags"], list) else data["hashtags"],
                        "call_to_action": data["cta"],
                        "image_prompt": data["image_prompt"],
                        "status": "processed"
                    }
                    return transformed_post
                else:
                    logger.error(f"Missing required fields in transformation response for post {post_key}")
                    
        except Exception as e:
            logger.error(f"Error parsing transformation response for post {post_key}: {e}")
            
        return None
    
    def _create_fallback_campaign_post(self, original_post: Dict, post_key: str, platform: str, username: str) -> Dict:
        """Create fallback post when transformation fails"""
        content_text = original_post.get("content", "")
        
        # Extract basic info from content
        sentences = content_text.split('. ') if content_text else []
        
        if len(sentences) >= 2:
            caption = '. '.join(sentences[:2])
        else:
            caption = content_text if content_text else "Discover something amazing today!"
        
        # Try to extract visual info from 3rd sentence for image prompt
        image_prompt = "High-quality, engaging visual that represents the brand aesthetic"
        if len(sentences) >= 3:
            third_sentence = sentences[2]
            if any(word in third_sentence.lower() for word in ["visual", "image", "photo", "picture", "look"]):
                image_prompt = third_sentence
        
        return {
            "caption": caption,
            "hashtag": "#Content #Inspiration #Engagement #Quality #Brand",
            "call_to_action": "What do you think? Share your thoughts!",
            "image_prompt": image_prompt,
            "status": "processed"
        }
    
